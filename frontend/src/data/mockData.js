// simple utilities
export const getPopularMangas = (limit) => {
  return mockMangas.slice(0, limit);
};

export const getTopRatedMangas = (limit) => {
  return [...mockMangas]
    .sort((a, b) => b.rating - a.rating)
    .slice(0, limit);
};

export const getMangaBySlug = (slug) => {
  return mockMangas.find(manga => manga.slug === slug) || null;
};

export const searchMangas = (query, limit) => {
  if (!query) return mockMangas.slice(0, limit);
  
  const lowerQuery = query.toLowerCase();
  return mockMangas
    .filter(manga => 
      manga.title.toLowerCase().includes(lowerQuery) ||
      manga.title_jp?.toLowerCase().includes(lowerQuery) ||
      manga.author?.toLowerCase().includes(lowerQuery) ||
      manga.description.toLowerCase().includes(lowerQuery) ||
      manga.tags?.some(tag => tag.toLowerCase().includes(lowerQuery))
    )
    .slice(0, limit);
};